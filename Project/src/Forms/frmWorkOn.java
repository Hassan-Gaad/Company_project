    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String select="select * from workon_data where ";
        if(rdoEmpNo.isSelected()){
        select+="eployee_no like '%"+txtSearch.getText()+"%'";
        }else if(rdoEmpName.isSelected()){
        select+="employee_name like '%"+txtSearch.getText()+"%'";
        }else if(rdoProjNo.isSelected()){
        select+="project_no like '%"+txtSearch.getText()+"%'";
        }else{
        select+="project_name like '%"+txtSearch.getText()+"%'";
        }
        wrk.getCustomRows(select, tblWorkOn);
        
    }//GEN-LAST:event_btnSearchActionPerformed

