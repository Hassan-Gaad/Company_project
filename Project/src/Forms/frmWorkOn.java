    public void setValues(){
    String GSI=cbxEmpName.getSelectedItem().toString();
    wrk.setEmpNo(Integer.parseInt(emp.getValueByName(GSI)));
    
    String getItem=cbxProjName.getSelectedItem().toString();
    wrk.setProjectNo(Integer.parseInt(pro.getValueByName(getItem)));
    
    }
    
